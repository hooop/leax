"""
ANSI Color Codes for Leax Terminal Output

Centralized color definitions used across the project.
"""

# Reset
RESET = "\033[0m"

# Green shades
GREEN = "\033[38;5;158m"
DARK_GREEN = "\033[38;5;49m"

# Yellow shades
LIGHT_YELLOW = "\033[38;5;230m"
DARK_YELLOW = "\033[38;5;228m"

# Pink/Magenta shades
LIGHT_PINK = "\033[38;5;225m"
MAGENTA = "\033[38;5;219m"
DARK_PINK = "\033[38;5;205m"

# Red
RED = "\033[38;5;174m"

# Gray shades
GRAY_DARK = "\033[38;5;236m"  # Used for code comments
GRAY = "\033[38;5;245m"  # Used for secondary info
